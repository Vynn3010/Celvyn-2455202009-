package Pertemuan2;

public class PerulanganFOR {

    // 3. Perulangan FOR
    public static void perulangan() {
        System.out.println("<<=== Looping FOR ===>>");
        for (int i = 1; i <= 5; i++) {
            System.out.println("i = " + i);
        }

        System.out.println();
    }

    // Method utama
    public static void main(String[] args) {
        perulangan();
    }
}
